/// Spacing constants for consistent layout throughout the app
class AppSpacing {
  /// Extra small spacing - 4px
  /// Use for: Micro adjustments, very tight element spacing
  static const double xs = 4.0;

  /// Small spacing - 8px
  /// Use for: Button groups, related element clusters, Wrap spacing
  static const double sm = 8.0;

  /// Medium spacing - 12px
  /// Use for: Content-to-action separation, moderate element spacing
  static const double md = 12.0;

  /// Large spacing - 16px
  /// Use for: Section internal padding, component breathing room
  static const double lg = 16.0;

  /// Extra large spacing - 24px
  /// Use for: Major section breaks, primary visual hierarchy
  static const double xl = 24.0;

  /// Extra extra large spacing - 32px
  /// Use for: Large layout gaps, major page sections
  static const double xxl = 32.0;

  /// Settings group gap - 24px
  /// Use for: Spacing between settings groups on settings pages
  static const double settingsGroupGap = 18.0;

  // Private constructor to prevent instantiation
  AppSpacing._();
}
